async function main() {
  console.log('🧠 IQ асуултуудыг MongoDB-д оруулж байна...\n');

  const mod = await prisma.module.findUnique({ where: { slug: 'iq' } });
  if (!mod) {
    console.log('⚠️  "iq" module олдсонгүй — эхлээд npm run db:seed ажиллуул');
    return;
  }

  await prisma.question.deleteMany({ where: { moduleId: mod.id } });
  console.log('🗑️  Хуучин IQ асуултууд устгагдлаа');

  await prisma.question.createMany({
    data: iqQuestions.map((q, i) => ({ ...q, order: i, moduleId: mod.id })),
  });

  await prisma.progress.updateMany({
    where: { moduleId: mod.id },
    data: { total: iqQuestions.length },
  });

  console.log(`✅ IQ: ${iqQuestions.length} асуулт (Казак + Монгол) MongoDB-д хадгалагдлаа`);
}

main().catch(console.error).finally(() => prisma.$disconnect());
